//  5. Check if a stack is empty.

const stack = []; // Example empty stack
const nonEmptyStack = [1, 2, 3]; // Example non-empty stack

// Check if a stack is empty
const isEmptyStack = stack.length === 0;
const isNonEmptyStackEmpty = nonEmptyStack.length === 0;

console.log("Is stack empty?", isEmptyStack); // Output: true
console.log("Is non-empty stack empty?", isNonEmptyStackEmpty); // Output: false

